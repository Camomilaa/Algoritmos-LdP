package edu.newton.ldp.av1;

public class Produto {
	public String nome;
	public String marca;
	public String dataDeFabricacao;
	public Float valor;
	
	public Produto(String nome, String marca, String dataDeFabricacao, Float valor) {
		this.nome = nome;
		this.marca = marca;
		this.dataDeFabricacao = dataDeFabricacao;
		this.valor = valor;
	}
	
}
